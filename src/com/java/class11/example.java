package com.java.class11;

public class example {
    public static void main(String[] args) {
        int num = 1;

        switch(num){
            case 0:
                System.out.println("zero");
                break;
            default:
                System.out.println("error");

            case 2:
                System.out.println("two");
                break;
            case 1:
                System.out.println("one");
        }
    }
}
