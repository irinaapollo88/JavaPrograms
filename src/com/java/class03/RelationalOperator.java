package com.java.class03;

public class RelationalOperator {
    public static void main(String[] args) {
        int a = 1, b = -1;
        System.out.println(a > b);// answer is true
        System.out.println(a < b);// answer is false
        System.out.println(a >= b);//answer is true
        System.out.println(a <= b);// answer is false
        System.out.println(a != b);// answer is true
        System.out.println(a == b);//answer is false
    }
    }